package frc.robot.utilities;

public class UnitConversions {

    /**
     * This class contains constants to convert between units
     * 
     * To convert units, multiply your value by the constant
     * 
     * While some of these conversions seem obvious, the intent of this class is to help make the
     * code as easy as possible to read and understand
     */

    public final double IN_TO_CM = 2.54;
    public final double CM_TO_IN = 1 / 2.54;
    public final double FT_TO_IN = 12;
    public final double IN_TO_FT = 1 / 12;
    public final double FT_TO_M = 0.3048;
    public final double M_TO_FT = 1 / 0.3048;
    public final double M_TO_CM = 100;
    public final double CM_TO_M = 1 / 100;
}